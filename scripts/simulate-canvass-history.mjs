// Seed a realistic month of demo canvassing history into the live database.
//
// What it creates (all tagged in a local manifest so --wipe can undo it):
//   - 1 new team ("Field Ops", assigned to the UBI campaign)
//   - 59 real auth accounts with fake people names: 2 campaign managers,
//     7 squad leaders (team_lead), 50 canvassers — avatars, accent colors,
//     bios, and phone numbers filled in so /roster and /member look lived-in
//   - ~26 active canvassing days ending yesterday: per-day squads (each
//     leader + usually 2 canvassers, fresh squads every day, correct
//     squad_date) so squad history is analyzable
//   - ~18k knock_logs generated by a door-by-door behavioral model
//
// The behavioral model (the part that makes analysis interesting):
//   - 7 geographic zones (Marysville split in 4 by street centroids, Plain
//     City, Richwood+Raymond, Dublin+Milford) each with its own latent
//     support level and answer rate; streets vary around their zone and
//     households around their street, so no street is a monotone run of one
//     outcome but areas still differ measurably.
//   - Each household has a latent answer propensity: some doors answer on
//     knock 1, some only on attempt 3-4 (and may sign then) — so P(answer)
//     as a function of attempt number is a real, discoverable curve.
//   - Person-level decisions: signed / maybe (followed up 4-9 days later
//     with a higher close rate) / didn't sign (never re-asked). A spouse
//     sometimes signs during the same visit.
//   - Households where everyone signed or everyone refused are never
//     re-knocked; hostile and skip (dog / no-trespassing) doors are closed
//     forever.
//   - Crews prioritize fresh doors early in the campaign; the revisit share
//     ramps from ~5% to ~35% over the month. Routes walk streets in
//     house-number order with per-door timestamps (weekday evening shifts,
//     longer weekend shifts).
//
// Usage (from repo root — hits the LIVE database on a real run):
//   node scripts/simulate-canvass-history.mjs --dry-run   # model + stats, no writes
//   node scripts/simulate-canvass-history.mjs             # create everything
//   node scripts/simulate-canvass-history.mjs --wipe      # remove it all (reads sim-manifest.json)
//
// Reads SUPABASE_SERVICE_ROLE_KEY from the gitignored KEYS-AND-ACCESS.md.
// Writes SIM-USERS.md (shared demo password + roster) and sim-manifest.json,
// both gitignored.

