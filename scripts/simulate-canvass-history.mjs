function buildZones(addresses) {
  // Group addresses into streets (city + street name, number parsed off).
  const streets = new Map()
  for (const a of addresses) {
    const m = a.street.match(/^(\d+)\s+(.+)$/)
    const num = m ? Number(m[1]) : 0
    const name = m ? m[2] : a.street
    const key = `${a.city}|${name}`
    let s = streets.get(key)
    if (!s) streets.set(key, (s = { key, city: a.city, name, doors: [], lat: 0, lng: 0, nGeo: 0 }))
    s.doors.push({ ...a, num })
    if (a.lat != null && a.lng != null) {
      s.lat += a.lat
      s.lng += a.lng
      s.nGeo++
    }
  }
  for (const s of streets.values()) {
    if (s.nGeo) {
      s.lat /= s.nGeo
      s.lng /= s.nGeo
    } else {
      s.lat = null
      s.lng = null
    }
    s.doors.sort((a, b) => a.num - b.num)
  }

  const zones = ZONE_DEFS.map((z) => ({ ...z, streets: [] }))

  // Marysville: quadrants around the median street centroid; streets with no
  // geocode get dealt round-robin so whole streets still stay together.
  const mvStreets = [...streets.values()].filter((s) => s.city === 'Marysville')
  const geo = mvStreets.filter((s) => s.lat != null)
  const lats = geo.map((s) => s.lat).sort((a, b) => a - b)
  const lngs = geo.map((s) => s.lng).sort((a, b) => a - b)
  const midLat = lats[Math.floor(lats.length / 2)]
  const midLng = lngs[Math.floor(lngs.length / 2)]
  let rr = 0
  for (const s of mvStreets) {
    let zi
    if (s.lat != null) {
      zi = s.lat >= midLat ? (s.lng < midLng ? 0 : 1) : s.lng < midLng ? 2 : 3
    } else {
      zi = rr++ % 4
    }
    zones[zi].streets.push(s)
  }
  for (const z of zones) {
    if (!z.cities) continue
    for (const s of streets.values()) if (z.cities.includes(s.city)) z.streets.push(s)
  }

  // Walk order: greedy nearest-neighbor over street centroids starting at the
  // west edge — days progress through a zone like a route, not alphabetically.
  for (const z of zones) {
    const withGeo = z.streets.filter((s) => s.lat != null)
    const noGeo = z.streets.filter((s) => s.lat == null).sort((a, b) => a.name.localeCompare(b.name))
    const ordered = []
    if (withGeo.length) {
      withGeo.sort((a, b) => a.lng - b.lng)
      let cur = withGeo.shift()
      ordered.push(cur)
      while (withGeo.length) {
        let best = 0
        let bestD = Infinity
        for (let i = 0; i < withGeo.length; i++) {
          const d = (withGeo[i].lat - cur.lat) ** 2 + (withGeo[i].lng - cur.lng) ** 2
          if (d < bestD) {
            bestD = d
            best = i
          }
        }
        cur = withGeo.splice(best, 1)[0]
        ordered.push(cur)
      }
    }
    // Sprinkle the un-geocoded streets through the route so they get worked too.
    const step = Math.max(1, Math.floor(ordered.length / (noGeo.length + 1)))
    noGeo.forEach((s, i) => ordered.splice(Math.min(ordered.length, (i + 1) * step + i), 0, s))
    z.streets = ordered
    z.streets.forEach((s, i) => (s.order = i))
    // Mutate the door objects in place (no copies) — street noise gets
    // stamped on these same objects later and households must see it.
    for (const s of z.streets)
      for (const d of s.doors) {
        d.streetOrder = s.order
        d.streetKey = s.key
      }
    z.doorList = z.streets.flatMap((s) => s.doors)
    z.cursor = 0
  }
  return zones
}

// ---------------------------------------------------------------- households

function initHousehold(addr, zone, personsByHouse) {
  const streetNoise = addr._streetNoise // shared per street, set below
  const support = clamp(zone.support + streetNoise + randn(0, 0.12), 0.03, 0.92)
  const persons = (personsByHouse.get(addr.id) ?? []).map((p) => ({
    id: p.id,
    willing: clamp(support + randn(0, 0.08), 0.02, 0.95),
    state: 'unresolved', // unresolved | signed | refused | maybe
  }))
  return {
    id: addr.id,
    answerProp: clamp(randn(zone.answer, 0.16), 0.06, 0.85),
    persons,
    attempts: 0,
    priorNotHomes: 0,
    closed: persons.length === 0, // nothing to win at a door with no roster
    nextRetryAt: null, // day index
    maybePersonId: null,
  }
}

const householdOpen = (h) => !h.closed && h.persons.some((p) => p.state === 'unresolved' || p.state === 'maybe')

